/**
 * Очередь
 *
 * Работает по FIFO
 *
 * Наиболее эффективно реализовывать на двусвязном списке, а не на массиве
 * потому что на массиве удаление первого элемента это O(n)
 */

/**
 * Стек
 *
 * Работает по LIFO
 *
 * Реализовать можно и на массиве, и на связанном списке
 */

/**
 * Deque
 * Это структора объединение очереди и стека
 * Тот же связанный список, но немного доработанный
 * Есть head и tail
 * Нет вставки в середину
 */
